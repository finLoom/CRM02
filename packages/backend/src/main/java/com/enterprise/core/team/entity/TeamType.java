package com.enterprise.core.team.entity;

/**
 * Defines the different types of teams in the system.
 */
public enum TeamType {
    OPERATIONS,    // Operations teams handle day-to-day operational tasks
    SALES,         // Sales teams focus on customer acquisition
    SUPPORT,       // Support teams handle customer issues and troubleshooting
    PROJECT,       // Project teams are formed for specific projects
    DEVELOPMENT,   // Development teams work on software development
    ADMINISTRATIVE // Administrative teams handle internal business functions
}